export function isBankAccountStale(account) {
  if (!account?.lastVerifiedAt) return false;
  const verifiedDate = new Date(account.lastVerifiedAt);
  const twelveMonthsAgo = new Date();
  twelveMonthsAgo.setMonth(twelveMonthsAgo.getMonth() - 12);
  return verifiedDate < twelveMonthsAgo;
}

